<h5 style=" margin-left: 25px; ">b. Campañas adicionales</h5>
<p style=" margin-left: 25px; ">En fechas aleatorias, de acuerdo con las metas establecidas por la Línea de Negocio, puede haber consideración de abono de puntos por la participación en campañas estacionales o conceptos definidos que se notificarán en tiempo y forma, indicando el total de puntos asignados por dichos conceptos. Un ejemplo de esto, son los 50,000 puntos que cada Promotoría recibió en el lanzamiento de este sistema de puntos durante el Kick Off.</p>
<h4>Portal de Puntos Promesas Cumplidas</h4>
<h5>1. ¿Cómo accedo al Portal?</h5>
<p>A través de tu cuenta MetBook dando clic sobre el banner denominado “Promesas Cumplidas”</p>
<h5>2. ¿Cómo me registro en el Portal de Promesas Cumplidas?</h5>
<p>Cada Promotor cuenta con un registro único para acceder al sitio vía MetBook que se le notifica por correo electrónico, proporcionándole los datos de su cuenta (usuario y contraseña).</p>
<h5>3. ¿Qué hago al recibir los datos de mi Cuenta?</h5>
<p>Entra a MetBook, busca el banner de Promesas Cumplidas, ingresa tu usuario y contraseña enviados en el correo de notificación “Bienvenido a Promesas Cumplidas”. Asigna una nueva tu contraseña con las características que se indican y resguarda ambos datos (usuario y contraseña), para salvaguardar la seguridad de la información y el buen manejo de los puntos que te acredites.</p>
<h5>4. ¿Cómo cambiar mi contraseña?</h5>
<ul>
	<li>Ingresa a MetBook y da clic en el banner “Promesas Cumplidas”</li>
	<li>Aparecerá una ventana en cuyo menú, deberás seleccionar la sección “Mi Cuenta”</li>
	<li>Ingresa los datos solicitados</li>
	<li>El sistema te notificará el cambio exitoso de contraseña y cerrará tu sesión actual, solicitando</li>
	<li>iniciar sesión nuevamente con los datos que actualizaste. Adicionalmente enviará a tu correo electrónico con los datos de la nueva contraseña</li>
</ul>
<h5>5. ¿Para qué sirve Mi Cuenta?</h5>
<p>Sirve para mantenerte informado de todos los detalles del Programa en temas como:</p>
<ul>
	<li>Alta de usuarios Desarrolladores de Talento (sólo para Usuarios Promotores)</li>
	<li>Consulta de saldo en puntos</li>
	<li>Descarga del estado de cuenta</li>
	<li>Acceso al catálogo general y especial</li>
	<li>Canje de puntos</li>
</ul>
<h5>6. ¿Puedo delegar la administración de Mi Cuenta a un tercero?</h5>
<p>Otorgar los datos de acceso de tu cuenta a otra persona, es como si le dieras la firma de tu chequera ya que estos son la llave para acceder a tu saldo en puntos y canjearlos por producto del catálogo.</p>
<p>Por seguridad, no compartas con nadie tus datos de acceso al Portal.</p>
<h5>7. ¿Cómo sé que se abonan puntos a Mi Cuenta?</h5>
<p>Toda vez que se abonen puntos a tu cuenta te haremos llegar por correo electrónico desde la cuenta <a href="mailto://promesasdxn@metlife.com.mx">promesasdxn@metlife.com.mx</a>, un mensaje titulado “Notificación Abono de Puntos” el cual te informará el total de puntos abonados, invitándote a consultar tu Estado de Cuenta.</p>
<h5>8. ¿Quiénes tienen acceso al Abono de Puntos?</h5>
<p>Los puntos acreditados por cada Promotor son ingresados al sistema por la Línea de Negocio y los puntos transferidos Desarrolladores de Talento son responsabilidad de cada Promotor. En ambos casos, el sistema genera un correo de notificación informando el abono exitoso.</p>
<h5>9. ¿Los puntos acumulados tienen vigencia?</h5>
<p>Sí, todos los puntos obtenidos en un año, vencen el 30 de marzo del año siguiente, por lo que debes hacerlos efectivos antes de esta fecha -de no ser canjeados- se eliminarán para poder iniciar el abono de puntos obtenidos a partir del primer trimestre del nuevo ejercicio anual.</p>
<h5>10. Una vez concluida la vigencia de mis puntos, ¿Debo renovar mi usuario?</h5>
<p>No, tu usuario será el mismo y sólo se reportará el saldo de puntos en “ceros”, hasta el momento en que te empiecen a abonar puntos de la nueva vigencia.</p>
<h5>11. ¿Cómo realizo el canje de Puntos?</h5>
<ol>
	<li>Ingresa a MetBook y dirígete al banner de “Promesas Cumplidas”</li>
	<li>Ingresa con tu usuario y contraseña</li>
	<li>Dirígete a la sección de Catálogo</li>
	<li>Elige el/los artículos(s) que deseas canjear agregándolo(s) al carrito, con previa verificación de que tu saldo sea suficiente para adquirir cada artículo elegido. Esto lo podrás ir revisando en el extremo superior derecho de la pantalla</li>
	<li>Al terminar tu elección, da clic en el botón “Canje”</li>
	<li>Presiona “Aceptar” y quedará registrada tu solicitud</li>
	<li>Confirma que recibiste tus artículos en buen estado, esto mediante la encuesta de servicio que te haremos por correo electrónico.</li>
</ol>
<h5>12. ¿Qué sucede una vez que he realizado mi pedido?</h5>
