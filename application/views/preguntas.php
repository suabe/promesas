<h5>¿Qué beneficios obtengo con el Programa de Promesas Cumplidas?</h5>
<p>Con base en el Cuaderno de Incentivos de Negocio Individual Público para 2014, todo Promotor que logre los requisitos de calidad, conservación y cumplimiento de meta al cierre de cada trimestre, se hace acreedor a los beneficios del Programa de Promesas Cumplidas, que son:</p>
<ol>
	<li>Bono trimestral en su Estado de Cuenta</li>
	<li>Puntos canjeables por artículos para su Fuerza de Ventas</li>
	<li>Diplomas y pines para sus agentes y Desarrolladores de Talento que se acrediten logros del trimestre, con base en la lista proporcionada por la Promotoría al Gerente Comercial que lo atiende.</li>
</ol>
<h5>¿Para qué sirven los puntos?</h5>
<p>Los puntos que se acredita cada Promotoría sirven para canjearse por artículos de diferentes categorías (hogar, salud y belleza, cocina, oficina/escuela, deportes y viajes, entre otros) que estarán publicados en los catálogos del Portal Promesas Cumplidas.</p>
