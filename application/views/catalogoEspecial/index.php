	<h2>¿Cómo solicito estos artículos?</h2>
	<p><strong>Paso 1</strong> Llama a nuestro Centro de Atención, marcando 1167 3452 (local) y al 01 800 681 7779 para para el interior de la República, de lunes a viernes entre 9:00 y 21:00 horas, o bien los sábados de 9:00 a 15:00 horas (tiempo local de la Ciudad de México).</p>
	<p><strong>Paso 2</strong> Te atenderá un ejecutivo, indícale qué artículo te gustaría canjear y se te informarán las condiciones para adquirirlo, sus características específicas, si hay o no en existencia, así como el tiempo y forma de entrega.</p>
	<p><strong>Paso 3</strong> El ejecutivo que te atienda enviará la información / cotización a tu cuenta de correo. Las cotizaciones tienen vigencia y en el caso de los viajes, dependerá de las opciones ofertadas y su disponibilidad. Recomendamos canjear siempre con base en la última cotización que se haya solicitado.</p>
	<p><strong>Paso 4</strong> Para la autorización del canje, da clic en el botón “Confirmar pedido” del correo que se te envíe y proporciona los datos solicitados. Una vez aprobado, automáticamente se efectuará el cargo a tu saldo en puntos y el movimiento se verá reflejado en el Estado de Cuenta que puedes consultar de inmediato en el sistema.</p>
	<p><strong>Paso 5</strong> El envío de estos artículos depende de las implicaciones de compra de cada uno. Para los equipos de cómputo se tiene un plazo de 15 días hábiles de entrega; para viajes se continúa el proceso de reservación establecido por el ejecutivo.</p>
