	<p>A manera de ejemplo, quienes tengan participación de agentes en MDRT acreditarán 10,000 puntos por cada uno de los asistentes. El objetivo es que ustedes indiquen a sus participantes que pueden elegir opciones en ese nivel de puntos para que una vez elegidos se soliciten desde las cuentas del Promotor o Desarrollador de Talento.</p>
	<p>Cabe mencionar que los puntos por concepto de boletos o convenciones se entregarán hasta que tengamos resultados al cierre del año.</p>
	<p>A manera de sugerencia, los puntos que se acrediten por cumplimiento trimestral (200,000) requieren dividirse entre quienes alcanzaron su meta de venta, con un tope mínimo de 10 Agentes o Desarrolladores de Talento. En el archivo PDF les mandaremos la lista de las personas para quienes nos han pedido diploma y pin. En caso de no tener sus listados se lo solicitaremos antes de cargar los puntos correspondientes en su cuenta. </p>
	<p>Dicho de otra manera, la entrega de su lista de ganadores es el primer paso para abonar los puntos correspondientes.</p>
	<h5>Canje de puntos</h5>
	<p>Los puntos acreditados por cada usuario durante los tres primeros trimestres del año, podrán ser canjeados hasta el mes de noviembre como máximo, para lo cual la Línea de Negocio enviará un mensaje-flash durante el mes de octubre confirmando la fecha definitiva en que podrán canjear sus puntos.</p>
	<p>Posterior a esta fecha, que depende de los cortes presupuestales de la compañía, los usuarios podrán seguir revisando su saldo y las novedades del Portal. Sin embargo, el canje de puntos se reactivará hasta que sean publicados los resultados del último trimestre del año.</p>
	<p>Las cuentas habilitadas para canjear son las de los Usuarios Promotores y Desarrolladores de Talento, y para salvaguardar la seguridad de los puntos de cada cuenta- es necesario validar la identidad del usuario en dos momentos clave:</p>
	<ol>
		<li>Al ingresar al catálogo y</li>
		<li>Al efectuar un canje</li>
	</ol>
	<p>En el caso de la cuenta genérica de Agentes, ellos sólo podrán acceder al Catálogo y elegir productos para generar una lista de artículos deseados a manera de cotización que le pueden enviar a su Promotor para que se le otorgue un incentivo a su gusto.</p>
	<h5>Transferencia de puntos</h5>
	<p>Sólo los Promotores podrán transferir puntos a sus Desarrolladores de Talento. Al igual que en el proceso de canje, se requerirá una validación de seguridad, por lo que el Promotor deberá capturar su usuario y contraseña al efectuar la transferencia.</p>
	<h5>Vigencia de los puntos</h5>
	<p>Todos los puntos generados en un año vencen el 30 de marzo del año siguiente. Por lo anterior, es importantísimo que cada usuario haga efectivos sus puntos antes de esa fecha, puesto que a partir del primer día de abril el saldo de las cuentas arranca en “ceros” y empieza a alimentarse con los puntos derivados de los logros del primer trimestre en adelante.</p>
	<h5>Recuperación de puntos</h5>
	<p>Es la operación de retiro de saldo de las cuentas. Una vez efectuada en el sistema, se notifica a los usuarios involucrados. Algunas de las causas que pueden detonar una recuperación de puntos son:</p>
	<ol>
		<li>Abono de montos incorrectos</li>
		<li>Error en transferencias de las cuentas de Promotores a sus subcuentas</li>
		<li>Baja de una subcuenta</li>
		<li>Terminación de relaciones</li>
	</ol>
	<h5>Catálogos disponibles</h5>
	<p>El Portal de puntos Promesas Cumplidas pone a la disposición de sus usuarios dos espacios de consulta de productos a canjear:</p>
	<ol>
		<li>Catálogo (actualizado mensualmente por el proveedor)</li>
		<li>Catálogo Especial</li>
	</ol>
	<p>Para conocer los artículos disponibles, recomendamos consultar la versión actualizada de los catálogos en línea.</p>
	<h5>Canje de productos del Catálogo</h5>
	<p>Los artículos están seccionados en diversas categorías (hogar, oficina, belleza, etc.) y sólo se pueden adquirir con puntos acreditados bajo el programa de Promesas Cumplidas, por lo que es necesario contar con el saldo suficiente al momento de hacer un canje.</p>
	<h5>Canje de productos del Catálogo Especial</h5>
	<p>Este catálogo complementa los artículos ofertados en la opción anterior. Para canjear los productos del mismo, es necesario contactar al ejecutivo de Promesas Cumplidas, en los teléfonos: 1167-3452 local o lada sin costo 01800-681-77-79 de lunes a viernes entre 9:00 y 21:00 horas y sábados de 9:00 a 15:00 horas, (tiempo local de la Ciudad de México).</p>
	<h5>Tiempos de entrega del Catálogo</h5>
	<p>El canje exitoso de todos los productos del Catálogo se notifica vía correo electrónico un día hábil después de solicitado el producto. A partir de ese momento el usuario podrá rastrear su paquete en la página de mensajería que le haya sido asignada y lo recibirá en un máximo de 15 días hábiles después de confirmado el pedido.</p>
	<h5>Tiempos de entrega del Catálogo Especial</h5>
	<p>En el caso del Catálogo Especial será necesario contactar al Centro de Atención y dar seguimiento al proceso de acuerdo a lo estipulado en el Manual de Usuario en la sección de “Canje de artículos del Catálogo Especial”.</p>
	<p>Les pedimos considerar los tiempos de entrega en la planeación de sus eventos de premiación, para que los esfuerzos de arraigo derivados de esta herramienta sean exitosos.</p>
	<h5>Garantías</h5>
	<p>Todos los artículos canjeados cuentan con garantías de fabricación con una fecha de inicio con base en la factura de entrega, por lo que es de vital importancia que cuando se reciba un artículo se verifique su buen estado y funcionamiento antes de firmar el acuse de recepción.</p>
	<h5>Entrega de artículos</h5>
	<p>Si al recibir tu pedido, el empaque no contiene el artículo que solicitaste o si el empaque se encuentra maltratado y/o las cintas y sellos de seguridad rotos, no lo recibas y repórtalo de inmediato al Centro de Atención. El reporte se debe realizar en un lapso no mayor a 24 horas. Después de este período no se aceptará ninguna reclamación.</p>
	<h5>Servicio</h5>
	<p>Como atención a los usuarios, el proveedor subsanará durante los primeros 15 días naturales -contados a partir de la entrega del producto en el domicilio señalado por el cliente- cualquier defecto de fabricación ya sea reparando, cambiando algunas piezas o sustituyendo el producto en su totalidad, según sea el desperfecto y dependiendo del artículo de que se trate.</p>
	<p>Una vez emitida la reclamación de la falla o desperfecto, las piezas o productos que vayan a ser sustituidos son propiedad del proveedor.</p>
	<p>Para acceder a este servicio, es preciso llamar al Centro de Atención y especificar el número de guía de mensajería con el que se marcó el pedido, indicando las características del desperfecto / falla. Al hacer la solicitud de servicio se asignará un folio de reporte y el(los) producto(s) deberá(n) ser enviado a la dirección que el ejecutivo indique, en un plazo no mayor de 5 días posteriores de la fecha de levantamiento del folio.Si se envían después de este tiempo, se cancelará la solicitud de servicio.</p>
	<p>Los artículos deberán enviarse en su empaque original, incluyendo manuales, accesorios, etiqueta de envío y copia del acuse. Después de 15 días deberá hacerse efectiva la garantía que se otorga por del proveedor, distribuidor o fabricante, y que se encuentra en el interior del empaque del artículo.</p>
	<h5>Información para el usuario</h5>
	<p>El Portal incluye varios espacios en donde se puede consultar la información de perfil, el saldo en puntos y los movimientos ejecutados por los usuarios. Adicional a estos espacios, se desprenden notificaciones automáticas que llegan a las cuentas de correo electrónico de los usuarios involucrados en las operaciones y los procesos.</p>
	<h5>Notificaciones</h5>
	<p>Algunas de las notificaciones automáticas que recibirán los usuarios son: correo de bienvenida, cambio exitoso de contraseña y confirmación de abono de puntos,… entre otros.</p>
	<h5>Estado de Cuenta</h5>
	<p>Es el registro automático y puntual que lleva el Portal de todos los movimientos de los puntos ejecutados por los usuarios, con el fin de otorgarles información actualizada de su saldo disponible. El Estado de Cuenta solamente es visible para quien se firme con los datos de acceso de una cuenta específica, por lo que es de vital importancia que no compartas con nadie esta información.</p>
	<p>En el caso de los movimientos realizados por un Desarrollador de Talento, además de generar actualizaciones en el Estado de Cuenta del mismo, nuestro Portal envía notificaciones vía correo electrónico al Promotor que dio de alta esa cuenta.</p>
	<h5>Calendario de puntos</h5>
	<p>Es una función activa en la sección de Mi Cuenta, que permite tener una visión resumida de los movimientos mensuales efectuados por las cuentas activas, especificando brevemente el tipo de movimiento realizado sobre la fecha en que se efectuó.</p>
	<h5>Manual de Usuario</h5>
	<p>Es el documento que contiene a detalle del funcionamiento del Portal y de cada una de sus secciones. Está disponible como archivo descargable en versión PDF y puedes acceder a él en todo momento dando clic sobre el texto “Manual de Usuario”, que se encuentra ubicado en la parte inferior de la página.</p>
