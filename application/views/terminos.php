	 <p>Pueden acceder a este Portal: Promotores, Desarrolladores de Talento y Agentes con sus distintas funcionalidades a través de MetBook. Para ello a los Promotores y Desarrolladores de Talento cuentan con un usuario y contraseña que se les proporciona para su uso personal e intransferible, considerando que los puntos tienen una conversión en pesos y por ende, les otorgan la posibilidad de adquirir algun(os)artículo(s) contenido(s) en los catálogos. En el caso de los Agentes se manejará un usuario y contraseña genéricos para consultar el catálogo y generar una lista de artículos deseados a manera de cotización que su Promotor podrá conocer como base para otorgarle algún artículo a manera de reconocimiento.</p>
	 <h5>Cuentas de Acceso</h5>
	 <p>Se trata de los datos de identificación (usuario y contraseña) vinculados a la información de cada usuario: nombre completo, Promotoría a la que pertenece, su usuario MetBook, domicilio de la oficina de su Promotoría y correo electrónico donde recibirá las notificaciones.</p>
	 <h5>Cuenta Promotor</h5>
	 <p>Cada Promotoría tiene un usuario único identificado por su clave y el número 01, designada para el uso del Promotor o bien la persona encargada de la administración de los puntos en el Portal. Esta cuenta tiene la capacidad de generar subcuentas para Desarrolladores de Talento.</p>
	 <h5>Subcuenta</h5>
	 <p>Datos de identificación de los usuarios dados de alta bajo alguna de las 22 cuentas de los Promotores. Estas cuentas se denominan “Desarrolladores de Talento” y están compuestas por la clave de la Promotoría y números consecutivos del 02 en adelante.</p>
	 <h5>Editar información de los usuarios</h5>
	 <p>Para modificar los datos de la cuenta de un usuario, es necesario escribir al correo del Administrador del Portal <a href="mailto://promesasdxn@metlife.com.mx">promesasdxn@metlife.com.mx</a> informándole qué campo del formulario de alta de usuarios se debe actualizar y en un tiempo no mayor a 24 horas hábiles, se verá reflejado.</p>
	 <h5>Activación</h5>
	 <p>Cada usuario registrado queda activo una vez que se haya hecho el cambio de contraseña en el ingreso inicial. En el caso de las subcuentas de Desarrolladores de Talento, la activación e inactivación depende del Usuario Promotor que las genere.</p>
	 <h5>Sistema de Puntos</h5>
	 <p>El Portal funciona con base en un sistema de abono y canje de puntos que se acreditan de acuerdo al cumplimiento de los objetivos comerciales establecidos por la Línea de Negocio para apuntalar el crecimiento comercial.</p>
	 <h5>Puntos</h5>
	 <p>Son las unidades de medida utilizadas durante la vigencia del Programa Promesas Cumplidas que se asignan a las Cuentas de Promotor, siempre y cuando acredite los objetivos estipulados para su abono. Cada punto abonado equivale a 0.05 centavos (M/N).</p>
	 <h5>Movimientos</h5>
	 <p>Se refiere a toda operación ejecutada que involucre puntos. Entre ellos: abono, transferencia, canje y recuperación. Estos movimientos quedan registrados en el sistema con sus respectivas especificaciones (usuario, fecha y hora del movimiento).</p>
	 <h5>Abono de puntos</h5>
	 <p>A partir del arranque de operaciones de este Portal, todos los Promotores conocerán el total de puntos que han acumulado durante el primer semestre del año. En el caso de logros trimestrales alineados al Programa de Promesas Cumplidas, los resultados de quienes se acrediten puntos se publicarán con base en el Quién es Quién del trimestre recién terminado.</p>
